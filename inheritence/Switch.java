package com.company;

public class Switch {
    public void run()
    {
        int x=5;
        switch (x)
        {
            case (5):
                System.out.println("5");
                break;
            case (10):
                System.out.println("10");
                break;
            case (15):
                System.out.println("15");
                break;
            default:
                System.out.println("none");

        }
    }
}
